"""CLI: propeller-resolve"""

from pathlib import Path

import click
import yaml

from ..models import Pipeline
from ..pipeline import resolve, pipeline_to_dict


LOCK_HEADER = "# AUTO-GENERATED — DO NOT EDIT\n# This file is generated by `propeller-resolve`. Edit propeller.yaml instead.\n\n"


def _generate_mermaid(pipeline: Pipeline) -> str:
    """Generate a Mermaid flowchart from the resolved pipeline."""
    lines = ["graph TD"]

    for i, stage in enumerate(pipeline.stages):
        stage_node = f"stage_{stage.name}"
        lines.append(f"  {stage_node}([{stage.name}])")

        # Connect previous stage's steps to this stage node
        if i > 0:
            prev_stage = pipeline.stages[i - 1]
            for step in prev_stage.steps:
                # Only leaf steps (no one in the stage depends on them)
                dependents = {
                    s.project for s in prev_stage.steps if step.project in s.depends_on
                }
                if not dependents:
                    lines.append(f"  {step.project} --> {stage_node}")

        # Connect stage node to root steps (no in-stage deps)
        for step in stage.steps:
            if not step.depends_on:
                lines.append(f"  {stage_node} --> {step.project}")

        # In-stage dependency edges
        for step in stage.steps:
            for dep in step.depends_on:
                lines.append(f"  {dep} --> {step.project}")

    return "\n".join(lines) + "\n"


@click.command()
@click.option(
    "--base", required=True, type=click.Path(exists=True), help="Base pipeline.yaml"
)
@click.option(
    "--overrides",
    default=None,
    type=click.Path(exists=True),
    help="Override propeller.overrides.yaml (optional)",
)
@click.option("--output", required=True, type=click.Path(), help="Output lock file")
@click.option(
    "--propeller-dir", default=".propeller", help="Path to propeller framework"
)
@click.option(
    "--graph/--no-graph",
    default=True,
    help="Generate Mermaid graph alongside lock file",
)
def main(
    base: str, overrides: str | None, output: str, propeller_dir: str, graph: bool
) -> None:
    """Resolve a base pipeline with optional overrides."""
    pipeline = resolve(Path(base), Path(overrides) if overrides else None, propeller_dir)
    out = Path(output)
    content = yaml.dump(
        pipeline_to_dict(pipeline), default_flow_style=False, sort_keys=False
    )
    out.write_text(LOCK_HEADER + content)
    click.echo(f"Resolved pipeline written to {out}")

    if graph:
        graph_path = out.with_suffix(".md")
        mermaid = _generate_mermaid(pipeline)
        graph_path.write_text(f"# Pipeline Graph\n\n```mermaid\n{mermaid}```\n")
        click.echo(f"Pipeline graph written to {graph_path}")
